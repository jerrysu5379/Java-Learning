public class TestChar {
    public static void main(String[] args){
        char c1='A';
        char c2='你';
        char c3='\n';
        char c4='\'';
        System.out.println(c1);
        System.out.println(c3);
        System.out.println(c2);
        System.out.println(c4);
        //字符串不是基本数据类型，是类
        String str="你好，中国！";
        System.out.println(str);
    }
}